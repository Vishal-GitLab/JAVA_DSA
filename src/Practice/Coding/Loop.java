package src.Practice.Coding;

public class Loop {
    public static void main(String[] args) {
        int num = 1;
        while (num <= 10) {
            System.out.println(num);
            num++;
        }
        System.out.println();
        int count = 500;
        while(count >= 200 ) {
            System.out.println(count);
            count--;
        }
    }
}
