package src.OperatorIfElseandNumberSystem;

public class OrderOperator {
    public static void main(String[] args) {
        System.out.println(8-3*3);    // multiple operator ke liye BODMAS rule use hota hai vise bhi problem solve karne ke liye left to right hi jata hai

        System.out.println(9/3/3);  // same operator hai to left to right chalta hai
        System.out.println(9/(3/3 + 2));
    }
}
