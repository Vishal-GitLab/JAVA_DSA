package src.PracticeExp;

public class ArithmeticOperation {
    public static void main(String[] args) {
        int x = 45;
        int y = 6;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);

        double p = 23;
        double q = 67;
        System.out.println(p+q);
        System.out.println(p-q);
        System.out.println(p*q);
        System.out.println(p/q);
    }
}
